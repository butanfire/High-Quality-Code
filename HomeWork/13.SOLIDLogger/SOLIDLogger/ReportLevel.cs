namespace SOLIDLogger
{
    /// <summary>
    /// There are 5 types of messages logging.
    /// Enumeration is the easiest way to add/modify existing types.
    /// </summary>
    public enum ReportLevel
    {
        Info = 0,
        Warn = 1,
        Error = 2,
        Critical = 3,
        Fatal = 4
    }
}
